// Regenerates lib/modules/generated.ts from whatever is installed in modules/ (MOD-01
// Phase 2). Modules are compiled into the Next build, so their imports have to exist
// statically at build time — this writes them out. Runs automatically before every
// build/test, and again after an install or uninstall so the next build picks the
// change up. JonDash ships zero modules, so a stock install generates an empty list.
import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(import.meta.dirname, "..");
const MODULES_DIR = path.join(ROOT, "modules");
const OUT = path.join(ROOT, "lib", "modules", "generated.ts");

// Same slug rule the source manifest enforces, so a folder name can never inject code.
const ID_RE = /^[a-z0-9][a-z0-9-]{0,63}$/;

/** Installed module ids: a modules/<id>/module.ts(x) with a safe folder name. */
export function discoverModules(dir = MODULES_DIR) {
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return []; // no modules/ folder at all
  }
  return entries
    .filter((e) => e.isDirectory() && ID_RE.test(e.name))
    .map((e) => e.name)
    .filter((id) =>
      ["module.ts", "module.tsx"].some((f) => fs.existsSync(path.join(dir, id, f))),
    )
    .sort();
}

export function renderRegistry(ids) {
  const header =
    "// AUTO-GENERATED by scripts/gen-module-registry.mjs — do not edit.\n" +
    "// Regenerated before every build and after any module install/uninstall.\n" +
    'import type { ModuleDefinition } from "./types";\n';
  if (ids.length === 0) {
    return `${header}\nexport const INSTALLED: ModuleDefinition[] = [];\n`;
  }
  const imports = ids.map((id, i) => `import m${i} from "@/modules/${id}/module";`).join("\n");
  const list = ids.map((_, i) => `m${i}`).join(", ");
  return `${header}${imports}\n\nexport const INSTALLED: ModuleDefinition[] = [${list}];\n`;
}

export function writeRegistry() {
  const ids = discoverModules();
  const next = renderRegistry(ids);
  // Only write on change, so a no-op run can't invalidate the build cache.
  let current = null;
  try {
    current = fs.readFileSync(OUT, "utf8");
  } catch {
    /* first run */
  }
  if (current !== next) fs.writeFileSync(OUT, next, "utf8");
  return ids;
}

// Only act when run directly, so tests can import the helpers above.
if (process.argv[1] && path.resolve(process.argv[1]) === path.resolve(import.meta.filename)) {
  const ids = writeRegistry();
  console.log(
    ids.length ? `module registry: ${ids.length} installed (${ids.join(", ")})` : "module registry: none installed",
  );
}
