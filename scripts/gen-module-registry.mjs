// Regenerates lib/modules/generated.ts from whatever is installed in modules/ (MOD-01
// Phase 2). Modules are compiled into the Next build, so their imports have to exist
// statically at build time — this writes them out. Runs automatically before every
// build/test, and again after an install or uninstall so the next build picks the
// change up. JonDash ships zero modules, so a stock install generates an empty list.
import fs from "node:fs";
import path from "node:path";

const ROOT = path.resolve(import.meta.dirname, "..");
const MODULES_DIR = path.join(ROOT, "modules");
const OUT = path.join(ROOT, "lib", "modules", "generated.ts");
// Helpers (MOD-08) are generated the same way, from their own folder and entry file.
const HELPERS_DIR = path.join(ROOT, "helpers");
const HELPERS_OUT = path.join(ROOT, "lib", "helpers", "generated.ts");

// Same slug rule the source manifest enforces, so a folder name can never inject code.
const ID_RE = /^[a-z0-9][a-z0-9-]{0,63}$/;

/** Installed module ids: a modules/<id>/module.ts(x) with a safe folder name. */
export function discoverModules(dir = MODULES_DIR) {
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return []; // no modules/ folder at all
  }
  return entries
    .filter((e) => e.isDirectory() && ID_RE.test(e.name))
    .map((e) => e.name)
    .filter((id) =>
      ["module.ts", "module.tsx"].some((f) => fs.existsSync(path.join(dir, id, f))),
    )
    .sort();
}

export function renderRegistry(ids) {
  const header =
    "// AUTO-GENERATED by scripts/gen-module-registry.mjs — do not edit.\n" +
    "// Regenerated before every build and after any module install/uninstall.\n" +
    'import type { ModuleDefinition } from "./types";\n';
  if (ids.length === 0) {
    return `${header}\nexport const INSTALLED: ModuleDefinition[] = [];\n`;
  }
  const imports = ids.map((id, i) => `import m${i} from "@/modules/${id}/module";`).join("\n");
  const list = ids.map((_, i) => `m${i}`).join(", ");
  return `${header}${imports}\n\nexport const INSTALLED: ModuleDefinition[] = [${list}];\n`;
}

/** Installed helper ids: a helpers/<id>/helper.ts with a safe folder name. */
export function discoverHelpers(dir = HELPERS_DIR) {
  let entries;
  try {
    entries = fs.readdirSync(dir, { withFileTypes: true });
  } catch {
    return []; // no helpers/ folder at all
  }
  return entries
    .filter((e) => e.isDirectory() && ID_RE.test(e.name))
    .map((e) => e.name)
    .filter((id) => fs.existsSync(path.join(dir, id, "helper.ts")))
    .sort();
}

export function renderHelperRegistry(ids) {
  const header =
    "// AUTO-GENERATED by scripts/gen-module-registry.mjs — do not edit.\n" +
    "// Regenerated before every build and after any helper install/removal.\n" +
    'import type { HelperDefinition } from "./types";\n';
  if (ids.length === 0) {
    return `${header}\nexport const INSTALLED_HELPERS: HelperDefinition[] = [];\n`;
  }
  const imports = ids.map((id, i) => `import h${i} from "@/helpers/${id}/helper";`).join("\n");
  const list = ids.map((_, i) => `h${i}`).join(", ");
  return `${header}${imports}\n\nexport const INSTALLED_HELPERS: HelperDefinition[] = [${list}];\n`;
}

/** Write a generated file only when it changed, so a no-op run can't bust the build cache. */
function writeIfChanged(file, next) {
  let current = null;
  try {
    current = fs.readFileSync(file, "utf8");
  } catch {
    /* first run */
  }
  if (current !== next) fs.writeFileSync(file, next, "utf8");
}

export function writeRegistry() {
  const ids = discoverModules();
  writeIfChanged(OUT, renderRegistry(ids));
  const helperIds = discoverHelpers();
  writeIfChanged(HELPERS_OUT, renderHelperRegistry(helperIds));
  return ids;
}

/** Helper ids written on the last run — for the CLI summary. */
export function currentHelperIds() {
  return discoverHelpers();
}

// Only act when run directly, so tests can import the helpers above.
if (process.argv[1] && path.resolve(process.argv[1]) === path.resolve(import.meta.filename)) {
  const ids = writeRegistry();
  const helpers = currentHelperIds();
  console.log(
    ids.length ? `module registry: ${ids.length} installed (${ids.join(", ")})` : "module registry: none installed",
  );
  console.log(
    helpers.length ? `helper registry: ${helpers.length} installed (${helpers.join(", ")})` : "helper registry: none installed",
  );
}
